public class ArithmeticOperatorExercise03 {//Arithmetic - 算数
											//Operator - 运算符
											//Exercise - 练习
	
	public static void main(String[] args) {
		
		int i1 = 10;
		int i2 = 20;
		int i = i1++;
		System.out.print("i = " + i);
		System.out.println("i2 = " + i2);
		i = --i2;
		System.out.print("i = " + i);
		System.out.println("i2 = " + i2);
	}
}